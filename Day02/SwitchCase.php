<?php 
switch ('Green'){
case 'Red':
    echo "Apple is Red!";
    break;
case 'Green':
    echo "Mango is Green!";
    break;
case 'Yellow':
    echo "Banana is Yellow";
    break;
case 'Black':
    echo "BlackBerry is Black";
    break;
default:
    echo "Fruits are Healthy!";
}
?>